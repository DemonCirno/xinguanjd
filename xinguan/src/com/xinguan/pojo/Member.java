package com.xinguan.pojo;

public class Member {
	private int memberID;
	private String memberName;
	private String memberNickname;
	private boolean memberSex;
	private int memberStuID;
	private String memberPhone;
	private String password;
	private String imagePath;
	
	public int getMemberID() {
		return memberID;
	}
	public void setMemberID(int memberID) {
		this.memberID = memberID;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberNickname() {
		return memberNickname;
	}
	public void setMemberNickname(String memberNickname) {
		this.memberNickname = memberNickname;
	}
	public boolean isMemberSex() {
		return memberSex;
	}
	public void setMemberSex(boolean memberSex) {
		this.memberSex = memberSex;
	}
	public int getMemberStuID() {
		return memberStuID;
	}
	public void setMemberStuID(int memberStuID) {
		this.memberStuID = memberStuID;
	}
	public String getMemberPhone() {
		return memberPhone;
	}
	public void setMemberPhone(String memberPhone) {
		this.memberPhone = memberPhone;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	
	
}
